L'objectif principal de Medicall est de permettre à des pesonnes qui ont de besoin de soins pour eux ou pour leurs proches de pouvoir en bénéficier à domicile. <br>
Cela permet :
<ul>
    <li>d'éviter de faire des queues interminables au niveau des hopitaux</li>
    <li>de bénéficier d'une prise en charge rapide et efficace</li>
    <li>de benéficier de soins particuliers et adaptés</li>
    <li>de pouvoir bénéficier d'un service d'assistance d'urgence </li>
</ul>

